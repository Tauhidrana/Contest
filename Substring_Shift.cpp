using namespace std;
using ll = long long;
void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    int i = 0;
    while (i < n && s[i] != 'z')
        i++;

    while (i < n && s[i] == 'z')
    {
        s[i] = 'a';
        i++;
    }

    cout << s << endl;
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }

    return 0;
}

