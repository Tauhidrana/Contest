using namespace std;
using ll = long long;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
    {
        int x, y;
        cin >> x >> y;
        ll z = y / x;
        if (z == 0)
            z = 1;

        ll cost1 = llabs(y - x * z);
        ll cost2 = llabs(y - x * (z + 1));

        cout << min(cost1, cost2) << endl;
    }

    return 0;
}

