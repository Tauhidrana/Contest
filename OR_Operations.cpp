using namespace std;
using ll = long long;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--)
    {
        int n;
        cin >> n;

        vector<int> a(n);

        int total = 0;

        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
            total |= a[i];
        }

        int seg = 0, cur = 0;

        for (int i = 0; i < n; i++)
        {
            cur |= a[i];

            if (cur == total)
            {
                seg++;
                cur = 0;
            }
        }

        cout << n - seg << endl;
    }

    return 0;
}

