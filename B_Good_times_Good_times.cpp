using namespace std;
using ll = long long;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--)
    {
        int x;
        cin >> x;

        int len = 0;
        ll tmp = x;

        while (tmp > 0)
        {
            len++;
            tmp /= 10;
        }

        ll p = 1;

        for (int i = 0; i < len; i++)
            p *= 10;

        cout << p + 1 << endl;
    }

    return 0;
}

